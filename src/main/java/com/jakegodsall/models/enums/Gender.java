package com.jakegodsall.models.enums;

/**
 * Enum representing grammatical gender categories.
 * This enum is used to categorize nouns, pronouns, and adjectives
 * by gender, which can vary depending on the language.
 */
public enum Gender {

    /**
     * Represents masculine gender.
     */
    MASCULINE,

    /**
     * Represents feminine gender.
     */
    FEMININE,

    /**
     * Represents neuter gender.
     */
    NEUTER
}